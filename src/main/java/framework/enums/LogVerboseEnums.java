package framework.enums;

/**
 * This enum holds the possible {@link org.apache.logging.log4j.Level LogLevels}
 */
public enum LogVerboseEnums {

	/**
	 * debug log level
	 * 
	 * @see org.apache.logging.log4j.Level#DEBUG
	 */
	DEBUG,

	/**
	 * error log level
	 * 
	 * @see org.apache.logging.log4j.Level#ERROR
	 */
	ERROR,

	/**
	 * info log level
	 * 
	 * @see org.apache.logging.log4j.Level#INFO
	 */
	INFO,

	/**
	 * ALL log level
	 *
	 * @see org.apache.logging.log4j.Level#ALL
	 **/
	ALL,

	/**
	 * fatal log level
	 * 
	 * @see org.apache.logging.log4j.Level#FATAL
	 */
	FATAL,

	/**
	 * off log level
	 * 
	 * @see org.apache.logging.log4j.Level#OFF
	 */
	OFF,

	/**
	 * trace log level
	 * 
	 * @see org.apache.logging.log4j.Level#TRACE
	 * 
	 **/
	TRACE,

	/**
	 * warn log level
	 *
	 * @see org.apache.logging.log4j.Level#WARN
	 **/
	WARN

}
